class Movie extends Item{
	public Movie(String title, String dir, String act, String genre, String age, String year, String path, int star, String story, String review) {
		this.title=title;
		this.dir=dir;
		this.act=act;
		this.genre=genre;
		this.age=age;
		this.year=year;
		this.star=star;
		this.story=story;
		this.review=review;
		this.path=path;
	}
}
